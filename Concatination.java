package Demo;

public class Concatination {
	
	
	public static void main(String[] args) {
		String add;
		String addition;
		
		add= "10"+"20"+"ABC";
		System.out.println(add);
		
		addition= "ABC"+"10"+"20";
		System.out.println(addition);
		
		String add1;
		String addition1;
		add1= 10+20+"ABC";
		System.out.println(add1);
		
		addition1= "ABC"+10+20;
		System.out.println(addition1);
			
		
	}

}
